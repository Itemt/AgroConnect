def get_departments():
    """Retorna lista de departamentos"""
    return [(dept, dept) for dept in sorted(COLOMBIA_LOCATIONS.keys())]

def get_cities_by_department(department):
    """Retorna ciudades de un departamento específico"""
    cities = COLOMBIA_LOCATIONS.get(department, [])
    return [(city, city) for city in sorted(cities)]

def get_all_cities():
    """Retorna todas las ciudades organizadas por departamento"""
    all_cities = []
    for dept, cities in COLOMBIA_LOCATIONS.items():
        for city in cities:
            all_cities.append((f"{city}, {dept}", f"{city}, {dept}"))
    return sorted(all_cities)
